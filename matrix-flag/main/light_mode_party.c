// バック用キャンバス (例: 32x32 を想定)
static uint8_t back_canvas[BACK_TOTAL_HEIGHT][BACK_TOTAL_WIDTH];

// 再生位置管理
static int current_frame = 0;

void reset_light_mode_party() {
    current_frame = 0;
}

void light_mode_party() {
    // 現在のフレームを back_canvas に書き込む
    for(int y = 0; y < BACK_TOTAL_HEIGHT; y++){
        for(int x = 0; x < BACK_TOTAL_WIDTH; x++){
            // gif_frames[current_frame][y][x] は 0~(GIF_PALETTE_SIZE-1) のインデックス
            back_canvas[y][x] = gif_frames[current_frame][y][x];
        }
    }

    // バックマトリクスへ描画 & Flush
    back_matrix_draw_from_palette(gif_palette, GIF_PALETTE_SIZE, back_canvas);
    flush_back_and_wait();

    // 次のフレームへ
    current_frame = (current_frame + 1) % GIF_NUM_FRAMES;

    // フレームレート調整 (100ms待つ = 10fps)
    vTaskDelay(40 / portTICK_PERIOD_MS);
}
